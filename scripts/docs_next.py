def markdown_index(root: Path, include_archive: bool) -> tuple[dict, str]:
    rows = []
    for path in files(root, (".md", ".mdx", ".rst", ".adoc"), include_archive=include_archive):
        text = read(path)
        headings = re.findall(r"^(#{1,6})\s+(.+)$", text, re.MULTILINE)
        links = re.findall(r"\[[^\]]+\]\(([^)]+)\)", text)
        rows.append(
            {
                "path": rel(path, root),
                "title": title_from_markdown(text, path.stem),
                "summary": first_paragraph(text),
                "lines": line_count(text),
                "headings": [{"level": len(h[0]), "title": h[1].strip()} for h in headings],
                "links": links,
                "sha256": sha256_file(path),
            }
        )
    data = {"include_archive": include_archive, "count": len(rows), "documents": rows}
    md = provenance("Markdown Index", [r["path"] for r in rows])
    md += f"- Markdown-like files indexed: {len(rows)}\n\n"
    md += md_table(["Title", "Lines", "Path"], [[r["title"], r["lines"], f"`{r['path']}`"] for r in rows])
    return data, md


def mermaid_counts(text: str) -> dict[str, int]:
    edge_matches = re.findall(r"^\s*[\w\u4e00-\u9fff]+\s+(?:[-.=]+>|---)", text, re.MULTILINE)
    node_matches = re.findall(r"^\s*[\w\u4e00-\u9fff]+(?:\[[^\]]+\]|\([^)]+\)|\{[^}]+\})", text, re.MULTILINE)
    return {"nodes": len(node_matches), "edges": len(edge_matches)}


def diagram_index(root: Path) -> tuple[dict, str]:
    rows = []
    for path in files(root, (".mmd",), include_archive=False):
        if "formal/SSBX/diagrams" not in rel(path, root):
            continue
        text = read(path)
        svg = path.with_suffix(".svg")
        rows.append(
            {
                "path": rel(path, root),
                "svg": rel(svg, root) if svg.exists() else "",
                "svg_exists": svg.exists(),
                "lines": line_count(text),
                **mermaid_counts(text),
                "sha256": sha256_file(path),
            }
        )
    data = {"count": len(rows), "diagrams": rows}
    md = provenance("Diagram Index", [r["path"] for r in rows])
    md += md_table(["Diagram", "SVG", "Nodes", "Edges", "Lines"], [[r["path"], r["svg"] or "missing", r["nodes"], r["edges"], r["lines"]] for r in rows])
    return data, md


def source_index(root: Path, include_archive: bool) -> dict:
    all_paths = []
    for suffixes in [(".lean",), (".md", ".mdx", ".rst", ".adoc"), (".mmd", ".svg", ".txt")]:
        for path in files(root, suffixes, include_archive=include_archive):
            all_paths.append(path)
    records = []
    for path in sorted(set(all_paths), key=lambda p: rel(p, root)):
        records.append(
            {
                "path": rel(path, root),
                "suffix": path.suffix,
                "bytes": path.stat().st_size,
                "sha256": sha256_file(path),
            }
        )
    return {"include_archive": include_archive, "count": len(records), "sources": records}


def crossrefs(root: Path, markdown_data: dict, lean_data: dict) -> tuple[dict, str]:
    lean_paths = {r["path"] for r in lean_data["records"]}
    lean_names: dict[str, list[str]] = {}
    for record in lean_data["records"]:
        for decl in record["declarations"]:
            lean_names.setdefault(decl["name"].strip("«»"), []).append(record["path"])

    rows = []
    for doc in markdown_data["documents"]:
        text = read(root / doc["path"])
        tokens = set(re.findall(r"`([^`]+)`", text))
        path_like = set(re.findall(r"(?:formal|scripts|web|义理|史料|六表_实虚史真)/[^\s`)，。；、]+", text))
        mentions = sorted({p for p in lean_paths if p in path_like or p in text})
        symbol_hits = []
        for name in sorted(tokens):
            lookup = name.strip("`").strip("«»")
            if lookup in lean_names:
                symbol_hits.append({"symbol": lookup, "paths": lean_names[lookup][:3]})
        rows.append({"document": doc["path"], "lean_path_mentions": mentions, "symbol_hits": symbol_hits[:20]})

    data = {"documents": rows}
    md = provenance("Cross References", ["Lean declarations", "Markdown documents"])
    md += md_table(
        ["Document", "Lean path mentions", "Symbol hits"],
        [[r["document"], len(r["lean_path_mentions"]), len(r["symbol_hits"])] for r in rows],
    )
    return data, md


def trust_boundary(root: Path) -> tuple[dict, str]:
    def code_lines_without_comments(text: str) -> list[str]:
        out: list[str] = []
        in_block = 0
        for line in text.splitlines():
            i = 0
            buf: list[str] = []
            while i < len(line):
                if in_block:
                    if line.startswith("-/", i):
                        in_block -= 1
                        i += 2
                    else:
                        i += 1
                    continue
                if line.startswith("/-", i):
                    in_block += 1
                    i += 2
                    continue
                if line.startswith("--", i):
                    break
                buf.append(line[i])
                i += 1
            out.append("".join(buf))
        return out

    rows = []
    for path in files(root, (".lean",), include_archive=False):
        text = read(path)
        for lineno, line in enumerate(code_lines_without_comments(text), start=1):
            s = line.strip()
            for kind, pattern in [
                ("axiom", r"^axiom\s+([^:\s]+)"),
                ("opaque", r"^opaque\s+([^:\s]+)"),
                ("partial def", r"^partial\s+def\s+([^(:=\s]+)"),
                ("unsafe", r"^unsafe\b"),
                ("sorry", r"^sorry\b"),
                ("admit", r"^admit\b"),
            ]:
                m = re.match(pattern, s)
                if m:
                    rows.append({"kind": kind, "name": m.group(1) if m.groups() else "", "path": rel(path, root), "line": lineno})
    data = {"records": rows}
    md = provenance("Trust Boundary Scan", ["formal/**/*.lean"])
    md += md_table(["Kind", "Name", "Path", "Line"], [[r["kind"], r["name"], f"`{r['path']}`", r["line"]] for r in rows])
    return data, md


def build_generated(root: Path, out: Path, include_archive: bool, web_payload: bool) -> list[GeneratedFile]:
    lean_data, lean_md = lean_index(root)
    registry_data, registry_md = registry_index(root)
    claim_data, claim_md = claim_index(root)
    operator_data, operator_md = operator_index(root)
    cell192_transition_data, cell192_transition_md = cell192_transition_index(root)
    cell192_tree_data, cell192_tree_md = cell192_tree_index(root)
    markdown_data, markdown_md = markdown_index(root, include_archive)
    diagram_data, diagram_md = diagram_index(root)
    crossrefs_data, crossrefs_md = crossrefs(root, markdown_data, lean_data)
    trust_data, trust_md = trust_boundary(root)
    sources = source_index(root, include_archive)

    payloads = {
        "sources": sources,
        "lean-index": lean_data,
        "registry-index": registry_data,
        "claim-index": claim_data,
        "operator-index": operator_data,
        "cell192-transition-index": cell192_transition_data,
        "cell192-tree-index": cell192_tree_data,
        "markdown-index": markdown_data,
        "diagram-index": diagram_data,
        "crossrefs": crossrefs_data,
        "trust-boundary": trust_data,
    }
    markdowns = {
        "lean-index": lean_md,
        "registry-index": registry_md,
        "claim-index": claim_md,
        "operator-index": operator_md,
        "cell192-transition-index": cell192_transition_md,
        "cell192-tree-index": cell192_tree_md,
        "markdown-index": markdown_md,
        "diagram-index": diagram_md,
        "crossrefs": crossrefs_md,
        "trust-boundary": trust_md,
    }

    generated: list[GeneratedFile] = []
    manifest_files = []
    for name, data in payloads.items():
        p = out / f"{name}.json"
        c = stable_json(data)
        generated.append(GeneratedFile(p, c))
        manifest_files.append({"path": rel(p, root), "sha256": sha256_text(c)})
    for name, content in markdowns.items():
        p = out / f"{name}.md"
        generated.append(GeneratedFile(p, content))
        manifest_files.append({"path": rel(p, root), "sha256": sha256_text(content)})

    if web_payload:
        web_data = {
            "meta": {"version": GENERATOR_VERSION, "rootName": root.name},
            "texts": markdown_data["documents"],
            "diagrams": diagram_data["diagrams"],
            "formal": lean_data["records"],
        }
        c = "window.SSBX_DATA = " + stable_json(web_data)
        generated.append(GeneratedFile(out / "web-data.js", c))
        manifest_files.append({"path": rel(out / "web-data.js", root), "sha256": sha256_text(c)})

    manifest = {
        "generator": GENERATOR_VERSION,
        "include_archive": include_archive,
        "generated_files": sorted(manifest_files, key=lambda r: r["path"]),
    }
    generated.append(GeneratedFile(out / "_manifest.json", stable_json(manifest)))
    return generated


def write_generated(files_to_write: list[GeneratedFile]) -> None:
    for item in files_to_write:
        item.path.parent.mkdir(parents=True, exist_ok=True)
        item.path.write_text(item.content, encoding="utf-8")


def clean_generated(out: Path) -> None:
    if out.exists():
        shutil.rmtree(out)


def compare_generated(root: Path, out: Path, generated: list[GeneratedFile]) -> list[str]:
    expected = {item.path.resolve(): item.content for item in generated}
    problems: list[str] = []
    for path, content in expected.items():
        if not path.exists():
            problems.append(f"missing generated file: {rel(path, root)}")
            continue
        current = path.read_text(encoding="utf-8")
        if current != content:
            problems.append(f"stale generated file: {rel(path, root)}")
    if out.exists():
        for path in out.rglob("*"):
            if path.is_file() and path.resolve() not in expected:
                problems.append(f"extra generated file: {rel(path, root)}")
    return sorted(problems)


def check_links(root: Path) -> list[str]:
    problems: list[str] = []
    docs = root / "docs-next"
    if not docs.exists():
        return ["docs-next directory is missing"]
    for path in sorted(docs.rglob("*.md")):
        if DOCS_GENERATED_PART[1] in path.relative_to(root).parts:
            continue
        text = read(path)
        for target in re.findall(r"\[[^\]]+\]\(([^)]+)\)", text):
            target = target.strip()
            if not target or target.startswith(("#", "http://", "https://", "mailto:")):
                continue
            target = target.split("#", 1)[0]
            target = target.replace("%20", " ")
            resolved = (path.parent / target).resolve()
            if not resolved.exists():
                problems.append(f"broken link in {rel(path, root)} -> {target}")
    return problems


def cmd_build(args: argparse.Namespace) -> int:
    root = args.root.resolve()
    out = (root / args.out).resolve() if not args.out.is_absolute() else args.out
    generated = build_generated(root, out, args.include_archive, args.web_payload)
    if args.clean:
        clean_generated(out)
    write_generated(generated)
    print(f"wrote {len(generated)} generated files to {rel(out, root)}")
    return 0


def cmd_check(args: argparse.Namespace) -> int:
    root = args.root.resolve()
    out = (root / args.out).resolve() if not args.out.is_absolute() else args.out
    generated = build_generated(root, out, args.include_archive, args.web_payload)
    problems = compare_generated(root, out, generated)
    if args.strict:
        problems.extend(check_links(root))
        diagram_json = next((g for g in generated if g.path.name == "diagram-index.json"), None)
        if diagram_json:
            diagram_data = json.loads(diagram_json.content)
            for item in diagram_data["diagrams"]:
                if not item["svg_exists"]:
                    problems.append(f"missing diagram svg: {item['path']}")
    if problems:
        for p in sorted(problems):
            print(p)
        return 1
    print("docs-next generated files are current")
    return 0


def cmd_list_sources(args: argparse.Namespace) -> int:
    root = args.root.resolve()
    data = source_index(root, args.include_archive)
    for item in data["sources"]:
        print(item["path"])
    return 0


def cmd_clean(args: argparse.Namespace) -> int:
    root = args.root.resolve()
    out = (root / args.out).resolve() if not args.out.is_absolute() else args.out
    clean_generated(out)
    print(f"removed {rel(out, root)}")
    return 0


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--root", type=Path, default=Path.cwd())
    sub = parser.add_subparsers(dest="cmd", required=True)

    for name in ["build", "check", "list-sources", "clean"]:
        p = sub.add_parser(name)
        p.add_argument("--out", type=Path, default=DEFAULT_OUT)
        p.add_argument("--include-archive", action="store_true")
        p.add_argument("--web-payload", action="store_true")
        if name == "build":
            p.add_argument("--clean", action="store_true")
        if name == "check":
            p.add_argument("--strict", action="store_true")

    args = parser.parse_args(argv)
    if args.cmd == "build":
        return cmd_build(args)
    if args.cmd == "check":
        return cmd_check(args)
    if args.cmd == "list-sources":
        return cmd_list_sources(args)
    if args.cmd == "clean":
        return cmd_clean(args)
    raise AssertionError(args.cmd)


if __name__ == "__main__":
    sys.exit(main())
