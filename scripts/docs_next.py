        {"level": 5, "kind": "yaoPrefix", "count": 32, "meaning": "五爻前缀"},
        {"level": 6, "kind": "hexagram", "count": 64, "meaning": "六爻成卦"},
        {"level": 7, "kind": "cell192", "count": 192, "meaning": "每卦配 已/今/未 三时态"},
