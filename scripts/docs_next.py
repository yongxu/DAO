    def bit_label(bit_string: str) -> str:
        return "".join("阳" if bit == "1" else "阴" for bit in bit_string)

    def patterns(width: int) -> list[str]:
        return ["".join(p) for p in itertools.product([b[0] for b in bits], repeat=width)]

